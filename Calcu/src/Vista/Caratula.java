    public void iniciarCalcu(){
        try{
            if (operacion==null){
                operacion = new Operacion(3552, "localhost" , this);
                operacion.start();
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Error en la entrada de datos");
            operacion=null;
        }
    }
    
